const char mp3Head[]= {0x49,0x44,0x33,0x03,0x00};
const char flacHead[]= {0x66,0x4C,0x61,0x43,0x00};
const char oggHead[]= {0x4F,0x67,0x67,0x53,0x00};
const char m4aHead[]= {0x00,0x00,0x00,0x18,0x00};
char buffer[BUFFER_SIZE];
char name[NAME_SIZE];

char getKey(size_t i) {
	if (i>=0) {
		if(i>0x7FFF)
			i%=0x7FFF;
	} else i=0;
	return key[(i*i+80923)%256];
}

int main(int argc,char **argv) {
	if(argc==1) {
		printf("Please drag in file\n");
		system("pause");
		return 0;
	}
	mkdir("out");
	int fileCount;
	size_t i,j,len;
	for(fileCount=1; fileCount<argc; fileCount++) {
		strcpy(name,argv[fileCount]);
		FILE *in=fopen(name,"rb");
		if(!in) {
			printf("Error: failed to open %s\n",name);
			continue;
		}
		printf("%s --> ",name);

		//read head and rename
		bool flag=1;
		len=strlen(name);
		for(i=len-1; name[i]!='.'; i--);
		j=i;
		for(; i>0&&name[i]!='\\'; i--)
			if(flag==1&name[i]=='['&&name[i-1]==' ') {
				j=i-1;
				flag=0;
			}
		i+=(i!=0);
		len=j-i;
		for(j=0; j<len; j++)
			name[j]=name[j+i];
		name[len]='\0';

		fread(buffer,4,1,in);
		for(i=0; i<4; i++)
			buffer[i]^=getKey(i);
		buffer[4]='\0';
		if(strcmp(buffer,mp3Head)==0)
			strcat(name,".mp3");
		else if(strcmp(buffer,flacHead)==0)
			strcat(name,".flac");
		else if(strcmp(buffer,oggHead)==0)
			strcat(name,".ogg");
		else if(strcmp(buffer,m4aHead)==0)
			strcat(name,".m4a");
		else {
			strcat(name,".bin");
			printf("Warning: Decryption may fail.\n");
		}

		strcpy(buffer,"out\\");
		strcat(buffer,name);
		FILE *out=fopen(buffer,"wb");
		if(!out) {
			printf("Error: failed to write %s\n",buffer);
			continue;
		}

		fwrite(buffer,4,1,out);

		//decrypt
		size_t count,front=4;
		while(count=fread(buffer,1,BUFFER_SIZE,in),count!=0) {
			for(i=0; i<count; i++)
				buffer[i]^=getKey(front+i);
			fwrite(buffer,count,1,out);
			front+=count;
		}
		printf("Success\n");
		fclose(in);
		fclose(out);
	}
	return 0;
}
